            <h3>Formation</h3>
            <p>
                Chez F&J, nous pensons qu’une culture financière est un droit et surtout un devoir pour toute partie prenante au développement de l’entreprise :
                chefs d’entreprise, administrateurs, conseils juridiques, employés, actionnaires en bourse...
                Dans un monde idéal, tous devraient avoir une bonne culture financière sous-tendant leurs analyses et prises de décision.
                Une compréhension partagée des enjeux financiers permet un meilleur développement de l’entreprise. Les administrateurs, dirigeants et conseils
                prennent ainsi de meilleures décisions stratégiques et les managers de meilleures décisions opérationnelles. Quant aux employés qui comprennent mieux l’impact
                de leurs actions sur la santé financière de l’entreprise, ils se sentent plus impliqués au quotidien et acceptent mieux les décisions du management.
            </p>

            <p>
                Afin de vous donner une idée objective de votre aisance ou de celle de vos collaborateurs avec les finances, F&J vous offre d’évaluer gratuitement
                votre QI financier ou celui de vos collaborateurs grâce à notre test anonyme en ligne, dont les résultats seront communiqués individuellement,
                puis agrégés pour le compte du management.
            </p>

            <p>
                En nous fondant sur ces résultats, nous vous proposerons un accompagnement sur-mesure pour développer
                la culture financière au sein de votre entreprise. Cette assistance prendra en compte le contexte particulier de votre organisation,
                ainsi que son vocabulaire financier. Elle inclura des formations appropriées au niveau de responsabilité des participants, qui seront
                mises en œuvre grâce à notre méthode inédite de formation qui allie sens logique, outils de visualisation ludiques et pratique de cas réels.
            </p>

            <p>
                Nous vous accompagnerons également dans la mise en œuvre d’une stratégie de dissémination de l’information financière au sein de votre entreprise,
                notamment à l’occasion d’évènements internes majeurs ou d’arrêtés comptables intermédiaires.
            </p>

            <p>
                <b>F&J ADVISORY est habilité comme cabinet privé de formation par le Fonds de Développement de la Formation Professionnelle (FDFP).</b>
